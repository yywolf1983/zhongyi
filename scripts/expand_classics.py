def med_id(name):
    name = name.strip()
    if name in name2id:
        return name2id[name]
    if name in HERB:
        d = HERB[name]
        i = next_med_id()
        d['id'] = i
        meds.append(d); name2id[name] = i
        return i
    if name in ('生地黄','熟地黄','火麻仁','炙甘草','陈皮','生姜','大枣','饴糖'):
        for alt in ('生地黄','熟地黄','火麻仁','炙甘草','陈皮','生姜','大枣','饴糖'):
            if name == alt and alt in name2id:
                return name2id[alt]
    raise SystemExit('未找到药材且未定义: ' + name)

