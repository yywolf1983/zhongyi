# ============================================================
# 2. Build effect -> [ids] reverse maps
# ============================================================
effect_to_formulas = {}
effect_to_medicines = {}

for f_item in formulas:
    for ename in (f_item.get('effects', []) or []):
        ename = ename.strip()
        if ename:
            effect_to_formulas.setdefault(ename, []).append(f_item['id'])

for m in medicines:
    for ename in (m.get('effects', []) or []):
        ename = ename.strip()
        if ename:
            effect_to_medicines.setdefault(ename, []).append(m['id'])

