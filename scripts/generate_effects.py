def generate_effect_info(name):
    """Generate description, mechanism, indications from TCM name."""
    desc_parts = []
    mech_parts = []
    inds_set = set()

    for keyword, (desc, mech, inds) in TERM_MAP.items():
        if keyword in name:
            desc_parts.append(desc)
            mech_parts.append(mech)
            for ind in inds:
                inds_set.add(ind)

    if not desc_parts:
        return {
            'description': f'{name}的治疗作用',
            'mechanism': f'通过相关药物的配伍，发挥{name}的作用',
            'indications': ['适应证']
        }
    else:
        return {
            'description': '；'.join(desc_parts),
            'mechanism': '；'.join(mech_parts),
            'indications': sorted(inds_set)
        }

