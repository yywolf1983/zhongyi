import json, os

DATA_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), '..', 'assets', 'data')

def load(fn):
    with open(os.path.join(DATA_DIR, fn), 'r', encoding='utf-8') as f:
        return json.load(f)

def save(fn, data):
    path = os.path.join(DATA_DIR, fn)
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f'  ✅ {fn}: {len(data)} 条')

medicines = load('medicines.json')
formulas = load('formulas.json')
acupres = load('acupuncture_prescriptions.json')
acupoints = load('acupoints.json')

med_map = {m['name']: m['id'] for m in medicines}
ap_map = {a['name']: a['id'] for a in acupoints}
mid_max = max(int(m['id'].split('_')[1]) for m in medicines)
fid_max = max(int(f['id'].split('_')[1]) for f in formulas)
pid_max = max(int(p['id'].split('_')[1]) for p in acupres)

def am(n,py,la,na,fl,me,ef,ind,us,co,ca,su):
    global mid_max
    if n in med_map: return
    mid_max+=1
    m={"id":f"medicine_{mid_max:03d}","name":n,"pinyin":py,"latin_name":la,"nature":na,"flavor":fl,"meridian":me,"effects":ef,"indications":ind,"usage":us,"contraindications":co,"category":ca,"subcategory":su}
    medicines.append(m)
    med_map[n]=m["id"]

def af(name, pinyin, cat, sub, src, author, ingredients, effects, indic, usage, modern, pharm, syn_ids, eff_ids, begin, adv):
    global fid_max; fid_max+=1
    fid=f'formula_{fid_max:03d}'
    ings=[]
    for mn,qty,role in ingredients:
        if mn not in med_map: return
        ings.append({"medicine_id":med_map[mn],"name":mn,"quantity":qty,"role":role})
    formulas.append({"id":fid,"name":name,"pinyin":pinyin,"category":cat,"subcategory":sub,"source":src,"author":author,"ingredients":ings,"effects":effects,"indications":indic,"usage":usage,"modern_applications":modern,"pharmacological_effect":pharm,"syndrome_ids":syn_ids,"effect_ids":eff_ids,"beginner_note":begin,"advanced_clinical_note":adv})

def ap(name, pinyin, cat, sub, syn, ap_data, effects, indic, method, modern, begin, adv):
    global pid_max; pid_max+=1
    pid=f'prescription_{pid_max:03d}'
    aps=[]; alt={'人中':'acupoint_335','华佗夹脊':'acupoint_086','阿是穴':'acupoint_090'}
    for an,am in ap_data:
        aid=ap_map.get(an) or alt.get(an)
        if not aid: return
        aps.append({"acupoint_id":aid,"name":an,"method":am})
    acupres.append({"id":pid,"name":name,"pinyin":pinyin,"category":cat,"subcategory":sub,"syndrome":syn,"acupoints":aps,"effects":effects,"indications":indic,"method":method,"modern_applications":modern,"beginner_note":begin,"advanced_clinical_note":adv})

