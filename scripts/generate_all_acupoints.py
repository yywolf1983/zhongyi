def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    project_dir = os.path.dirname(script_dir)
    output_path = os.path.join(project_dir, "assets", "data", "acupoints.json")

    acupoints = build_all_acupoints()

    print(f"生成穴位总数: {len(acupoints)}")

    # 统计每条经络的穴位数量
    meridian_counts = {}
    for ac in acupoints:
        m = ac["meridian"]
        meridian_counts[m] = meridian_counts.get(m, 0) + 1
    for m, c in meridian_counts.items():
        print(f"  {m}: {c}穴")

    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(acupoints, f, ensure_ascii=False, indent=2)
    print(f"已写入 {output_path}")


if __name__ == "__main__":
    main()
