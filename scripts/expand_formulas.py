import json, os

DATA_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), '..', 'assets', 'data')
BACKUP = os.path.join(os.path.dirname(os.path.abspath(__file__)), '..', 'assets', 'data_backup')

def load(fn):
    with open(os.path.join(DATA_DIR, fn), 'r', encoding='utf-8') as f:
        return json.load(f)

def save(fn, data):
    path = os.path.join(DATA_DIR, fn)
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f'  ✅ {fn}: {len(data)} 条')

def backup(fn):
    os.makedirs(BACKUP, exist_ok=True)
    with open(os.path.join(DATA_DIR, fn), 'r', encoding='utf-8') as src:
        with open(os.path.join(BACKUP, fn), 'w', encoding='utf-8') as dst:
            dst.write(src.read())

# Load all data
medicines = load('medicines.json')
formulas = load('formulas.json')
acupres = load('acupuncture_prescriptions.json')
acupoints = load('acupoints.json')

med_map = {m['name']: m['id'] for m in medicines}
acupoint_map = {}
for a in acupoints:
    acupoint_map[a['name']] = a['id']

mid_max = max(int(m['id'].split('_')[1]) for m in medicines)
fid_max = max(int(f['id'].split('_')[1]) for f in formulas)
pid_max = max(int(p['id'].split('_')[1]) for p in acupres)

# Helper
def add_med(name, pinyin, latin, nature, flavor, meridians, effects, indications, usage, contra, cat, sub):
    global mid_max
    if name in med_map:
        return med_map[name]
    mid_max += 1
    mid = f'medicine_{mid_max:03d}'
    medicines.append({"id":mid,"name":name,"pinyin":pinyin,"latin_name":latin,"nature":nature,"flavor":flavor,"meridian":meridians,"effects":effects,"indications":indications,"usage":usage,"contraindications":contra,"category":cat,"subcategory":sub})
    med_map[name] = mid
    return mid

def add_formula(name, pinyin, cat, sub, src, author, ingredients, effects, indic, usage, modern, pharm, syn_ids, eff_ids, begin, adv, rel_syn=None):
    global fid_max
    fid_max += 1
    fid = f'formula_{fid_max:03d}'
    ings = []
    for mn, qty, role in ingredients:
        mid = med_map.get(mn)
        if not mid:
            print(f'    ⚠️ 药物 {mn} 不存在({name})')
            return
        ings.append({"medicine_id":mid,"name":mn,"quantity":qty,"role":role})
    f = {"id":fid,"name":name,"pinyin":pinyin,"category":cat,"subcategory":sub,"source":src,"author":author,"ingredients":ings,"effects":effects,"indications":indic,"usage":usage,"modern_applications":modern,"pharmacological_effect":pharm,"syndrome_ids":syn_ids,"effect_ids":eff_ids,"beginner_note":begin,"advanced_clinical_note":adv}
    if rel_syn:
        f["related_syndromes"] = rel_syn
    formulas.append(f)

def add_pres(name, pinyin, cat, sub, syn, acupoints_data, effects, indic, method, modern, begin, adv):
    global pid_max
    pid_max += 1
    pid = f'prescription_{pid_max:03d}'
    aps = []
    for an, am in acupoints_data:
        aid = acupoint_map.get(an)
        if not aid:
            alt = {'阿是穴':'acupoint_090','局部阿是穴':'acupoint_178','腰夹脊':'acupoint_086','腰阳关':'acupoint_318','腰痛穴':'acupoint_178','胆囊穴':'acupoint_285'}
            aid = alt.get(an)
            if not aid:
                print(f'    ⚠️ 穴位 {an} 不存在({name})')
                return
        aps.append({"acupoint_id":aid,"name":an,"method":am})
    acupres.append({"id":pid,"name":name,"pinyin":pinyin,"category":cat,"subcategory":sub,"syndrome":syn,"acupoints":aps,"effects":effects,"indications":indic,"method":method,"modern_applications":modern,"beginner_note":begin,"advanced_clinical_note":adv})

