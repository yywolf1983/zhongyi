import json, os

DATA_DIR = os.path.join(os.path.dirname(os.path.dirname(__file__)), 'assets', 'data')

with open(os.path.join(DATA_DIR, 'formulas.json')) as f:
    existing = json.load(f)
with open(os.path.join(DATA_DIR, 'medicines.json')) as f:
    medicines = json.load(f)

med_map = {m['name']: m['id'] for m in medicines}

# 药物ID获取辅助函数
def get_mid(name):
    return med_map.get(name, None)

# 当前最大ID
max_id = max(int(f['id'].replace('formula_', '')) for f in existing)

new_formulas = []
fid = max_id

def add_formula(name, pinyin, category, subcategory, source, author, ingredients_data, effects, indications, modern, related):
    global fid
    fid += 1
    ingredients = []
    for name_qty_role in ingredients_data:
        mn, qty, role = name_qty_role
        mid = get_mid(mn)
        ingredients.append({
            "medicine_id": mid if mid else "",
            "name": mn,
            "quantity": qty,
            "role": role
        })
    return {
        "id": f"formula_{fid:03d}",
        "name": name, "pinyin": pinyin,
        "category": category, "subcategory": subcategory,
        "source": source, "author": author,
        "ingredients": ingredients,
        "effects": effects,
        "indications": indications,
        "modern_applications": modern,
        "related_syndromes": related
    }

